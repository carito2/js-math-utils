const showNumber = (number) => {
    console.log(`El número ingresado es ${number}`);
}

showNumber(25);

const sumNumbers = (numberOne, numberTwo) => {
    console.log(`La suma de ${numberOne} y ${numberTwo} es igual a ${numberOne+numberTwo}`)
}

sumNumbers(25, 2);

const rest = (a,b) => {
    console.log(`La resta de ${a} y ${b} es igual a ${a-b}`);
}

rest(3,4);

